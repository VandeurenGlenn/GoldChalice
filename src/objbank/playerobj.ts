export class Player {
  constructor(id, name, health, defense, weapon, location, area, progress, map) {
    this.id = id
    this.name = name
    this.health = health
    this.defense = defense
    this.weapon = weapon
    this.location = location
    this.area = area
    this.progress = progress
    this.map = map
  }
}

export default Player
